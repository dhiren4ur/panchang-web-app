<?php
declare(strict_types=1);

class Moon {

    public static function apparentLongitude(float $jd): float {

        $T = ($jd - 2451545.0) / 36525.0;

        // Mean longitude (deg)
        $L0 = 218.3164477 + 481267.88123421*$T
            - 0.0015786*$T*$T
            + $T*$T*$T/538841
            - $T*$T*$T*$T/65194000;

        // Mean elongation
        $D = 297.8501921 + 445267.1114034*$T
            - 0.0018819*$T*$T
            + $T*$T*$T/545868
            - $T*$T*$T*$T/113065000;

        // Sun anomaly
        $M = 357.5291092 + 35999.0502909*$T
            - 0.0001536*$T*$T
            + $T*$T*$T/24490000;

        // Moon anomaly
        $Mp = 134.9633964 + 477198.8675055*$T
            + 0.0087414*$T*$T
            + $T*$T*$T/69699
            - $T*$T*$T*$T/14712000;

        // Argument of latitude
        $F = 93.2720950 + 483202.0175233*$T
            - 0.0036539*$T*$T
            - $T*$T*$T/3526000
            + $T*$T*$T*$T/863310000;

        // ✅ CRITICAL FIX — normalize BEFORE trig
        $L0 = self::normalize($L0);
        $D  = self::normalize($D);
        $M  = self::normalize($M);
        $Mp = self::normalize($Mp);
        $F  = self::normalize($F);

        // Convert to radians
        $D  = deg2rad($D);
        $M  = deg2rad($M);
        $Mp = deg2rad($Mp);
        $F  = deg2rad($F);

        // Eccentricity correction
        $E = 1 - 0.002516*$T - 0.0000074*$T*$T;

        // ✅ FULL 60 TERMS (Meeus Table 45.A)
        $terms = [
            [0,0,1,0,6288774],[2,0,-1,0,1274027],[2,0,0,0,658314],
            [0,0,2,0,213618],[0,1,0,0,-185116],[0,0,0,2,-114332],
            [2,0,-2,0,58793],[2,-1,-1,0,57066],[2,0,1,0,53322],
            [2,-1,0,0,45758],[0,1,-1,0,-40923],[1,0,0,0,-34720],
            [0,1,1,0,-30383],[2,0,0,-2,15327],[0,0,1,2,-12528],
            [0,0,1,-2,10980],[4,0,-1,0,10675],[0,0,3,0,10034],
            [4,0,-2,0,8548],[2,1,-1,0,-7888],[2,1,0,0,-6766],
            [1,0,-1,0,-5163],[1,1,0,0,4987],[2,-1,1,0,4036],
            [2,0,2,0,3994],[4,0,0,0,3861],[2,0,-3,0,3665],
            [0,1,-2,0,-2689],[2,0,-1,2,-2602],[2,-1,-2,0,2390],
            [1,0,1,0,-2348],[2,-2,0,0,2236],[0,1,2,0,-2120],
            [0,2,0,0,-2069],[2,-2,-1,0,2048],[2,0,1,-2,-1773],
            [2,0,0,2,-1595],[4,-1,-1,0,1215],[0,0,2,2,-1110],
            [3,0,-1,0,-892],[2,1,1,0,-810],[4,-1,-2,0,759],
            [0,2,-1,0,-713],[2,2,-1,0,-700],[2,1,-2,0,691],
            [2,-1,0,-2,596],[4,0,1,0,549],[0,0,4,0,537],
            [4,-1,0,0,520],[1,0,-2,0,-487],[2,1,0,-2,-399],
            [0,0,2,-2,-381],[1,1,1,0,351],[3,0,-2,0,-340],
            [4,0,-3,0,330],[2,-1,2,0,327],[0,2,1,0,-323],
            [1,1,-1,0,299],[2,0,3,0,294]
        ];

        $sum = 0.0;

        foreach ($terms as $t) {

            [$d,$m,$mp,$f,$coeff] = $t;

            $arg = $d*$D + $m*$M + $mp*$Mp + $f*$F;

            $Efactor = 1.0;
            if (abs($m) == 1) $Efactor = $E;
            if (abs($m) == 2) $Efactor = $E*$E;

            $sum += $coeff * $Efactor * sin($arg);
        }

        // Final longitude
        $lambda = $L0 + $sum / 1000000.0;

        return self::normalize($lambda);
    }

    private static function normalize(float $angle): float {
        $angle = fmod($angle, 360.0);
        return ($angle < 0) ? $angle + 360.0 : $angle;
    }
}